async function main() {
  console.log('Seeding restaurants...');

  await prisma.dish.deleteMany();
  await prisma.restaurantHoraire.deleteMany();
  await prisma.restaurant.deleteMany();

  const restaurantMap = new Map<string, { id: string; nom: string }>();

  for (const data of restaurants) {
    const restaurant = await prisma.restaurant.create({
      data: {
        nom: data.nom,
        adresse: data.adresse,
        distinction: data.distinction,
        latitude: data.latitude,
        longitude: data.longitude,
        imageUrls: JSON.stringify(data.imageUrls),
        horaires: {
          create: data.horaires.map((h) => ({
            jour: h.jour,
            creneaux: JSON.stringify(h.creneaux),
          })),
        },
      },
    });
    restaurantMap.set(restaurant.nom, { id: restaurant.id, nom: restaurant.nom });
    console.log(`  ✓ ${restaurant.distinction}⭐ ${restaurant.nom}`);
  }

  console.log(`\nSeeding ${dishes.length} dishes...`);
  for (const dish of dishes) {
    const restaurant = restaurantMap.get(dish.restaurantName) ?? Array.from(restaurantMap.values())[0];
    await prisma.dish.create({
      data: {
        sourceKey: buildSourceKey(dish.restaurantName, dish.title),
        restaurantId: restaurant.id,
        title: dish.title,
        caption: dish.caption,
        imageUrl: dish.imageUrl,
        cuisine: dish.cuisine,
        mood: dish.mood,
        budget: dish.budget,
        prepTime: dish.prepTime,
        vegan: dish.vegan,
        ingredients: JSON.stringify(dish.ingredients),
        allergens: JSON.stringify(dish.allergens),
        tags: JSON.stringify(dish.tags),
        sourceUrl: dish.sourceUrl ?? null,
      },
    });
  }

  console.log(`\nDone — ${restaurants.length} restaurants and ${dishes.length} dishes créés.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
