function slugify(input: string) {
  return input
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

function buildSourceKey(restaurantName: string, title: string) {
  return `${slugify(restaurantName)}::${slugify(title)}`;
}

